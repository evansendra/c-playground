#include <iostream>

using namespace std;

int main()
{
	int first_argument;
	int second_argument;
	cout << "Enter first argument: ";
	cin >> first_argument;
	cout << "Enter second argument: ";
	cin >> second_argument;
	cout << first_argument << " * " << second_argument << " = " << first_argument * second_argument << endl;
	cout << first_argument << " + " << second_argument << " = " << first_argument + second_argument << endl;
	cout << first_argument << " / " << second_argument << " = " << first_argument / second_argument << endl;
	cout << first_argument << " - " << second_argument << " = " << first_argument - second_argument << endl;
}

